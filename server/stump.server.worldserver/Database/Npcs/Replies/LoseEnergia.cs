namespace Database.Npcs.Replies
{
    [Discriminator("LoseEnergia", typeof(NpcReply), new System.Type[]
 {
  typeof(NpcReplyRecord)
 })]
    class LoseEnergia : NpcReply
    {
        public LoseEnergia(NpcReplyRecord record)
            : base(record)
        {
        }
        public override bool Execute(Npc npc, Character character)
        {
            character.Energy -= 10000;
            character.RefreshActor();


            return true;
        }
    }
